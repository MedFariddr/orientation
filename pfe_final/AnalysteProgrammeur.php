        <h4 style="margin-left: 4%;color: #FF9009" >Les débouchés :</h4></p>
        <p style="margin-left: 6%">Principaux débouchés pour les férus d’informatique, les ESN (entreprises de services du numérique) proposent à elles seules près de 80 % des postes disponibles, notamment pour des postes de conseil en organisation et d’intégration de logiciels.Mais les ESN (ex-SSII) ne sont pas les seules. Banques, usines, administrations cherchent aussi à recruter des spécialistes en informatique pour la protection et la maintenance de leurs systèmes.</p>
