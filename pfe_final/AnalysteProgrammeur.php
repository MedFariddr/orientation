        <h2 style="margin-left: 2%;color:#118C4E">Analyste Programmeur</h2>
        <p style="margin-left: 4%">L’analyste-programmeur est un informaticien qui réalise des logiciels à partir d’un cahier des charges. Il utilise des langages informatiques et écrit et conçoit des lignes de codes.</p>
        <h4 style="margin-left: 4%;color: #FF9009" >Compétences nécessaires pour devenir analyste-programmeur :</h4></p>
        <ul style="margin-left: 4%">
            <li>Maîtrise de l’informatique dont la connaissance des différents langages de programmation.</li>
            <li>Savoir travailler en équipe.</li>
            <li>Un grand sens de l’écoute.</li>
            <li>Rigueur et précision.</li> 
            <li>Maîtrise de l’anglais indispensable.</li> 
        </ul>
        </article>
